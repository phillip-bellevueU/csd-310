public class Example_003{

  public static void main(String[] args){

    boolean [] booleanArray_02 = new boolean [3];
    byte [] byteArray_02 = new byte [3];
    char [] charArray_02 = new char [3];
    short [] shortArray_02 = new short [3];
    int [] intArray_02 = new int [3];
    float [] floatArray_02 = new float [3];
    double [] doubleArray_02 = new double [3];
    String [] stringArray_02 = new String [3];

    // Prints Array Reference
    System.out.println("Boolean array = " + booleanArray_02);
    System.out.println("Byte array = " + byteArray_02);
    System.out.println("Char array = " + charArray_02);
    System.out.println("Short array = " + shortArray_02);
    System.out.println("Integer array = " + intArray_02);
    System.out.println("Float array = " + floatArray_02);
    System.out.println("Double array = " + doubleArray_02);
    System.out.println("String array = " + stringArray_02);

    System.out.println();

    // Prints Array Elements Default Value Received
    System.out.println("Boolean array = '" + booleanArray_02[0] + "'");
    System.out.println("Byte array = '" + byteArray_02[0] + "'");
    System.out.println("Char array = '" + charArray_02[0] + "'");
    System.out.println("Short array = '" + shortArray_02[0] + "'");
    System.out.println("Integer array = '" + intArray_02[0] + "'");
    System.out.println("Float array = '" + floatArray_02[0] + "'");
    System.out.println("Double array = '" + doubleArray_02[0] + "'");
    System.out.println("String array = '" + stringArray_02[0] + "'");

    System.out.println();
  }
}